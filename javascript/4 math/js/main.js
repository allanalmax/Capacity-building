// Math methods and properties

console.log(Math.PI);

console.log(Math.trunc(Math.PI)); //returns the integer potion of a number and removes any decimals

console.log(Math.round(Math.PI)); //returns the value of the number rounded to the nearest integer

console.log(Math.ceil(Math.PI)); //returns the ceiling of the number rounded up

console.log(Math.floor(Math.PI)); //returns a number that will be rounded down

console.log(Math.pow(2, 4)); //to the power of

console.log(Math.min(2, 4, 6, 1)); //returns the smallest number

console.log(Math.max(2, 4, 6, 1)); //returns the largest number

console.log(Math.random()); //returns a random number from 0 to 1